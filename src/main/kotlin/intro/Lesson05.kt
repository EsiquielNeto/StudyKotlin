package intro
// Relational Operators

fun main(args: Array<String>) {
    val equal = 1 == 1
    val differ = 1 != 2
    val greaterThan = 2 > 1
    val lessThan = 1 < 2
    val greaterOrEqual = 3 >= 1
    val lessOrEqual = 1 <= 3

    println("equals $equal")
    println("differ $differ")
    println("greater than $greaterThan")
    println("less than $lessThan")
    println("greater or equal $greaterOrEqual")
    println("less or equal $lessOrEqual")

}